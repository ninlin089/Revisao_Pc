package exercicios;

public class operadores_atribuicao {

	public static void main(String[] args) {
		
		// Estes operadores atribuem um novo valor a uma variavel ou expressão.
		// o operador = apenas atribui um valor
		// os operadores +=, -=, *= e /= calculam e atribuem um novo valor
		
		int i = 10;
		
		int dois  = 1;
		dois += 1;            // dois = dois +1;
		
		int cinco = 7;
		cinco -= 2;          // cinco = cinco -2;
		     
		int dez = 5;
		dez *=2;             // dez = dez *2;
		
		int quatro = 12; 
		quatro /= 3;        //quatro = quatro / 3;
				

	}

}
