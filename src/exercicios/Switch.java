package exercicios;

public class Switch {

	public static void main(String[] args) {
		
		int numero = 1;
		switch(numero){
		case 1:
			 System.out.println("um");
			 break;
		case 2:
		     System.out.println("Dois");
			 break;
		case 3:
			 System.out.println("Tres");
			 break;
			 default:
				 System.out.println("Nenhum");
				 break;		
		} 

	}

}
